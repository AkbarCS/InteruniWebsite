            <p>Unlike regular competitions, tasks are self-set. This allows both those who might be looking to get their Silver distance as well as the seasoned XC pilot a platform for scoring.</p>
            <p>Cross country tasks, as in previous years, will be scored using the BGA ladder system, with the following differences:</p>
            <ol>
                <li>All tasks set or suggested in briefing will be treated as declared for the purposes of scoring.</li>
                <li>If anyone scores more than 1,000 points for a given day, all scores on that day will be scaled such that the maximum scored by anyone is 1,000 points.</li>
            </ol>
            <p>This scaling rule was implemented for the following reason: Previously, a day in which 5,000 points are scored by someone will completely overshadow a day in which the maximum score is only 500, and so the overall scores will only reflect who did well on the good days. By scaling the scores, they better reflect consistent flying across all the days.</p>
            <p>Lastly, make sure you've got a crew arranged before you set off! Aerotow retrieves will be available. We will do our best to set up a mutual system of retrieves.</p>
            <p>Cross country <code>igc</code> traces are uploaded to this site for scoring.</p>
