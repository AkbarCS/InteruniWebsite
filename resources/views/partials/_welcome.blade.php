    <div class="panel-body text-justify">
        <p>The Interuniversity Gliding Competition started in 1979, making this the 39th ‘Interunis’. This year it will be organised by University of Nottingham GC at Nympsfield, which is located between Bristol and Gloucester in the south west of England.</p>
		<p>The competition will officially start on Sunday 29th of July 2018 after the morning briefing and the last scoring day will be Saturday 4th of August. 
		The prize-giving ceremony will be in the evening of the 4th of August. You are welcome to come and fly on Saturday 28th and Sunday 5th, but we won’t score these days to allow universities for travel.</p>
		<p>The competition, (as usual) is split between three main categories:</p>
        <ul>
            <li>Progression</li>
            <li>Soaring</li>
            <li>Cross Country</li>
        </ul>
        <p>This should allow everyone to fairly represent their university, regardless of their experience. Motivation and hard work is the key to success.</p>
